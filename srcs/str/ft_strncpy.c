#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t count;

	count = 0;
	while (src[count] != '\0' && count < len)
	{
		dst[count] = src[count];
		count++;
	}
	while (count < len)
	{
		dst[count] = '\0';
		count++;
	}
	return (dst);
}

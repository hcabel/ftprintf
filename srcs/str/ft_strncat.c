#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t size;

	i = 0;
	size = ft_strlen(s1);
	while (i < n && s2[i] != '\0')
	{
		s1[i + size] = s2[i];
		i++;
	}
	s1[i + size] = '\0';
	return (s1);
}

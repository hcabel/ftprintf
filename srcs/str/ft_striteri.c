#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int i;
	unsigned int size;

	if (s == NULL || !f)
		return ;
	i = 0;
	size = ft_strlen(s);
	while (i < size)
	{
		f(i, &(s[i]));
		i++;
	}
}

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*str;

	i = 0;
	str = (char*)s;
	while (str[i] != '\0')
		i++;
	while (str[i] != c && i >= 0)
		i--;
	if (str[i] == c)
		return (str + i);
	return (0);
}

#include "libft.h"
#include <stdlib.h>

char	*ft_strnew(size_t size)
{
	char	*tab;
	size_t	i;

	if (!(tab = (char*)malloc(sizeof(*tab) * (size + 1))))
		return (NULL);
	i = -1;
	while (++i < size + 1)
		tab[i] = '\0';
	return (tab);
}
